# ── Constants ──────────────────────────────────────────────────────────────────
LABELS = [
    "toxicity", "severe_toxicity", "obscene",
    "threat", "insult", "identity_attack", "sexual_explicit"
]

THRESHOLD = 0.5          # score >= threshold → label = 1
MAX_VOCAB  = 30_000
MAX_SEQ_LEN = 200
PAD_TOKEN, UNK_TOKEN = "<PAD>", "<UNK>"

# ── Text cleaning ──────────────────────────────────────────────────────────────
def clean_text(text: str) -> str:
    """Lowercase, remove URLs, special chars, and extra whitespace."""
    text = str(text).lower()
    text = re.sub(r"http\S+|www\S+", "", text)          # remove URLs
    text = re.sub(r"[^a-z0-9\s!?.,']", " ", text)       # keep basic punctuation
    text = re.sub(r"\s+", " ", text).strip()
    return text

# ── Vocabulary ─────────────────────────────────────────────────────────────────
class Vocabulary:
    """Maps tokens to integer indices."""

    def __init__(self):
        self.token2idx = {PAD_TOKEN: 0, UNK_TOKEN: 1}
        self.idx2token = {0: PAD_TOKEN, 1: UNK_TOKEN}

    def build(self, texts: list[str], max_vocab: int = MAX_VOCAB):
        """Build vocab from a list of cleaned texts."""
        counter = Counter()
        for text in texts:
            counter.update(text.split()) #tokenize (Text: "This is very lame"  ---> ["This", "Is", "Very", "Lame"])
        most_common = counter.most_common(max_vocab - 2)
        for i, (token, _) in enumerate(most_common, start=2):
            self.token2idx[token] = i
            self.idx2token[i] = token

    def encode(self, text: str, max_len: int = MAX_SEQ_LEN) -> list[int]:
        """Convert text → padded list of indices."""
        tokens = text.split()[:max_len] #Trim sent with more than 200 token
        ids = [self.token2idx.get(t, 1) for t in tokens]   # 1 = UNK ----> Text Representation (for each word ) ---> (["Hi", "My", "Name", "Is"])
        ids += [0] * (max_len - len(ids))                  # 0 = PAD
                    # 200   -  190  ------> Add 10 Zeros
        return ids

    def __len__(self):
        return len(self.token2idx)

# ── PyTorch Dataset ────────────────────────────────────────────────────────────
class ToxicDataset(Dataset):
    """
    Wraps HuggingFace civil_comments split into a PyTorch Dataset.

    Args:
        hf_split : HuggingFace dataset split object
        vocab     : fitted Vocabulary instance
        max_len   : maximum sequence length
    """

    def __init__(self, hf_split, vocab: Vocabulary, max_len: int = MAX_SEQ_LEN):
        self.vocab   = vocab # Vocabulary() ---> build() / encode()
        self.max_len = max_len #200
        self.texts   = [clean_text(ex["text"]) for ex in hf_split] #df["clean_text"] = df["text"].apply(clean_text_func)
        self.labels  = [
            [float(ex[l] >= THRESHOLD) for l in LABELS]
            for ex in hf_split
        ]

    def __len__(self):
        return len(self.texts)

    def __getitem__(self, idx):
        ids    = self.vocab.encode(self.texts[idx], self.max_len)
        labels = self.labels[idx]
        return (
            torch.tensor(ids,    dtype=torch.long),
            torch.tensor(labels, dtype=torch.float),
        )
    
# ── Stratify to have the same distribution ───────────────────────────────────────────────────────

def safe_stratify(X, y, size, random_state=42):
    """Split with stratification, dropping rare label combos that would break it."""
    stratify_labels = y[LABELS].astype(int).astype(str).agg("".join, axis=1)
    
    # Drop rows whose label combo appears less than 2 times
    combo_counts = stratify_labels.value_counts()
    rare_combos = combo_counts[combo_counts < 2].index
    mask = ~stratify_labels.isin(rare_combos)
    
    X_filtered = X[mask]
    y_filtered = y[mask]
    stratify_filtered = stratify_labels[mask]
    
    if len(rare_combos) > 0:
        print(f"Dropped {(~mask).sum()} rows with rare label combos: {list(rare_combos)}")

    return train_test_split(
        X_filtered, y_filtered,
        train_size=size, random_state=random_state,
        stratify=stratify_filtered
    )

# ── Main loader function ───────────────────────────────────────────────────────
def get_dataloaders(
    batch_size: int = 64,
    max_vocab: int  = MAX_VOCAB,
    max_len: int    = MAX_SEQ_LEN,
    subset: int     = 200_000,
) -> tuple[DataLoader, DataLoader, DataLoader, Vocabulary]:

    print("Loading dataset from HuggingFace...")
    raw = load_dataset("google/civil_comments")

    dataset = pd.DataFrame(raw["train"])  # civil_comments only has a train split
    dataset[LABELS] = (dataset[LABELS] >= THRESHOLD).astype(int)

    # Build stratify label from all binary label columns
    dataset["stratify_label"] = (
        dataset[LABELS].astype(int).astype(str).agg("".join, axis=1)
    )

    combo_counts = dataset["stratify_label"].value_counts()
    rare_combos  = combo_counts[combo_counts < 2].index
    dropped      = dataset["stratify_label"].isin(rare_combos).sum()
    dataset      = dataset[~dataset["stratify_label"].isin(rare_combos)]
    print(f"Dropped {dropped} rows with rare label combos: {list(rare_combos)}")


    # --- Split once on the FULL dataset to preserve distribution ---
    X_temp, X_test, y_temp, y_test = train_test_split(
        dataset["text"], dataset[LABELS],
        test_size=0.15, random_state=42,
        stratify=dataset["stratify_label"]       # stratify on full dataset labels
    )
    temp_stratify = y_temp[LABELS].astype(int).astype(str).agg("".join, axis=1)

    X_train, X_val, y_train, y_val = train_test_split(
        X_temp, y_temp,
        test_size=0.15, random_state=42,
        stratify=temp_stratify                   # stratify on the temp subset's labels
    )

        # --- Subset train ---
    if subset and subset < len(X_train):
        X_train, _, y_train, _ = safe_stratify(X_train, y_train, size=subset)

    # --- Subset val and test ---
    if subset:
        val_size  = int(subset * 0.1)
        test_size = int(subset * 0.1)
        X_val,  _, y_val,  _ = safe_stratify(X_val,  y_val,  size=val_size)
        X_test, _, y_test, _ = safe_stratify(X_test, y_test, size=test_size)
        print(f"Split sizes — Train: {len(X_train)}, Val: {len(X_val)}, Test: {len(X_test)}")

    # Build vocabulary on training text only
    print("Building vocabulary...")
    train_texts = [clean_text(t) for t in X_train]   # X_train is a Series, not list of dicts
    vocab = Vocabulary()
    vocab.build(train_texts, max_vocab)
    print(f"Vocabulary size: {len(vocab)}")

    def to_dict_list(X, y):
        records = []
        for text, labels in zip(X, y.values.tolist()):
            row = {"text": text}
            for i, l in enumerate(LABELS):
                row[l] = labels[i]  # already binarized (0 or 1)
            records.append(row)
        return records

    # Pass the correct split variables to ToxicDataset
    train_ds = ToxicDataset(to_dict_list(X_train, y_train), vocab, max_len)
    val_ds   = ToxicDataset(to_dict_list(X_val,   y_val),   vocab, max_len)
    test_ds  = ToxicDataset(to_dict_list(X_test,  y_test),  vocab, max_len)

    train_loader = DataLoader(train_ds, batch_size=batch_size, shuffle=True,  num_workers=0)
    val_loader   = DataLoader(val_ds,   batch_size=batch_size, shuffle=False, num_workers=0)
    test_loader  = DataLoader(test_ds,  batch_size=batch_size, shuffle=False, num_workers=0)

    return train_loader, val_loader, test_loader, vocab

# ── Label weights for imbalance ────────────────────────────────────────────────
def compute_label_weights(loader: DataLoader) -> torch.Tensor:
    """
    Compute positive weight for each label (for BCEWithLogitsLoss).
    weight_i = (N - pos_i) / pos_i
    """
    all_labels = []
    for _, labels in loader:
        all_labels.append(labels)
    all_labels = torch.cat(all_labels, dim=0)
    pos = all_labels.sum(dim=0)
    neg = all_labels.shape[0] - pos
    weights = neg / (pos + 1e-6)
    return weights

